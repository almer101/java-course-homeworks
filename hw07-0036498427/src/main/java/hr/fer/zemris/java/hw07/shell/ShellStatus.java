package hr.fer.zemris.java.hw07.shell;

/**
 * This is the enumeration which represents the status or the
 * state of the shell. It can be continue or terminate.
 * 
 * @author ivan
 *
 */
public enum ShellStatus {

	/**
	 * The shell can continue with the work.
	 */
	CONTINUE,
	
	/**
	 * The shell must terminate the work.
	 */
	TERMINATE;
}
